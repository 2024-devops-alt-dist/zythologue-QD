create table beer (
	id serial primary key,
	name varchar(255) not null,
	description text not null,
	abv float,
	acidity int check (acidity >= 1 and acidity <= 5),
	bitterness int check (bitterness >= 1 and bitterness <= 5),
	sweetness int check (sweetness >= 1 and sweetness <= 5),
	container_type varchar(50),
	beer_volume int,
	organic_beer bool
);

create table users (
	id serial primary key,
	lastname varchar(255) not null,
	firstname varchar(255) not null,
	email varchar(90) not null,
	password varchar(255) not null
);

create table ingredient (
	id serial primary key,
	name varchar(255) not null,
	type varchar(100)
);

create table category (
	id serial primary key,
	name varchar(255) not null,
	description text,
	brewing_method varchar(50)
);

create table photo (
	id serial primary key,
	id_beer int,
	url varchar(500),
	accessibility text,
	constraint fk_beer foreign key(id_beer) references beer(id)
);

create table brewery (
	id serial primary key,
	id_photo int,
	name varchar(255) not null,
	description text,
	address varchar(500),
	city int,
	country varchar(100),
	organic_beer bool,
	history text,
	constraint fk_photo foreign key(id_photo) references photo(id)
);

create table review (
	id serial primary key,
	id_beer int,
	id_users int,
	title varchar(255) not null,
	text text,
	rate int check (rate >= 1 and rate <= 5),
	country varchar(100),
	created_at timestamp,
	updated_at timestamp,
	verify bool,
	constraint fk_beers foreign key(id_beer) references beer(id),
	constraint fk_users foreign key(id_users) references users(id)
);

create table beer_category (
	id serial primary key,
	id_beer int,
	id_category int,
	constraint fk_beer_category foreign key(id_beer) references beer(id),
	constraint fk_category_beer foreign key(id_category) references category(id)
);

create table beer_ingredient (
	id serial primary key,
	id_beer int,
	id_ingredient int,
	constraint fk_beer_ingredient foreign key(id_beer) references beer(id),
	constraint fk_ingredient_beer foreign key(id_ingredient) references ingredient(id)
);

create table beer_brewery (
id serial primary key,
id_beer int,
id_brewery int,
constraint fk_beer_brewery foreign key(id_beer) references beer(id),
constraint fk_brewery_beer foreign key(id_brewery) references brewery(id)
);

create table beer_users (
id serial primary key,
id_beer int,
id_users int,
constraint fk_beer_users foreign key(id_beer) references beer(id),
constraint fk_users_beer foreign key(id_users) references users(id)
);

